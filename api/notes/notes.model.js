const mongoose = require('mongoose');

const { Schema } = mongoose;
const notesSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  body: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('note', notesSchema);
